#obj  标签说明 

    #p #obj = 对象名称
    
    #p #p = 对象属性（类变量），名称后如果有“=”，则“=”后的文字为该属性的说明
    
    #p #m = 对象功能（类方法），名称后如果有“=”，则“=”后的文字为该属性的说明

    #p #f = 功能流程

    #p #i = 跟在类、属性、方法之后，用于解释说明
    
    #p ## = 同上，工程师想要被提取到文档的注释

    #p creator|owner|create|... = 创建人，文字声明到代码的声明，可以根据实际情况增加匹配值

    #p createtime|ctime|... = 创建时间，可以根据实际情况增加匹配值

    #p editor|updater|u@|... = 更新人，可以根据实际情况增加匹配值

    #p updatetime|lastUpdate|lu|... = 更新时间，可以根据实际情况增加匹配值
    
    #p  help|sos|... = 工程师代码过程中遇到困难，可以在对应的类、方法、代码块 添加help的帮助说明
    
#obj = 对象呈现

    #p docName = 产品定义对对象名称

    #p codeName = 代码中实际的类名称

    #p 说明 = 产品定义的对象说明，以及工程师添加的注释说明

    #p 属性 = 类的变量，也是一个对象，呈现和对象类似

    #p 功能 = 类的方法，也是一个对象

    #p 创建人creator = 创建类的人，即按照python语法定义codeName的人

    #p 创建时间createtime = 类按照python语法被定义的时间

    #p 更新人editor = 更新类的人，比如加了注释、添加了中间变量、中间方法

    #p 更新时间uptimetime = 更新类的时间

    #m 查看属性 = 查看属性这个对象的具体内容

    #m 查看方法 = 查看功能的具体内容


#obj = 对象属性呈现

    #p docName = 产品定义的属性名称

    #p codeName = 代码中实际的变量名称

    #p 说明 = 产品定义的属性说明

    #p 创建人creator = 创建类的人，即按照python语法定义codeName的人

    #p 创建时间createtime = 类按照python语法被定义的时间

    #p 更新人editor = 更新类的人，比如加了注释、添加了中间变量、中间方法

    #p 更新时间uptimetime = 更新类的时间




#obj = 对象功能呈现：

